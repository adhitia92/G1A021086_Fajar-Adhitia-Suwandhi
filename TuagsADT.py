# disini saya membuat suatu penerapan ADT sederhana berupa menghitung luas Bangun datar, lebih tepatnya menghitung luas segitiga 
# menggunakan bahasa pemrograman berupa Python, disini alas merupakan suatu perintah untuk memanggil input berupa panjang alas segitia yang akan dimasukan oleh pengguna
# tinggi merupakan suatu perintah untuk memanggil input berupa panjang segitiga yang akan dimasukan oleh pengguna
# int(alas)/(tinggi) merupakan mengubah nilai yang ditentukan menjadi integer, dan (*) merupakan simbol perkalian untuk membuat rumus luas segitiga 

alas = input("Panjang Alas Segitiga?(CM) ") 
tinggi = input("Panjang Tinggi segitiga?(CM) ")
print("Jadi Luas Segitiga Adalah: ", int(alas) * int(tinggi) * 0.5, "CM2")
